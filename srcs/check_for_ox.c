#include "ft_printf.h"

int		ft_check_ox(char *str)
{
	size_t i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '0' && (str[i + 1] == 'x' || str[i + 1] == 'X'))
			return (1);
		i++;
	}
	return (0);
}
